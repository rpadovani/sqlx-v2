package testutil

import (
	"github.com/google/go-cmp/cmp"
)

// ErrorStringComparer allows go-cmp to compare two errors based on their
// .Error() string values rather than by checking strict type equality or
// memory address equality.
var ErrorStringComparer = cmp.Comparer(func(e1, e2 error) bool {
	if e1 == nil && e2 == nil {
		return true
	}
	if e1 == nil || e2 == nil {
		return false
	}
	return e1.Error() == e2.Error()
})
